*/

Route::get('/', function () {
    return view('welcome');
});

Route::middleware([
    'auth:sanctum',
    config('jetstream.auth_session'),
    'verified',
])->group(function () {
    Route::get('/dashboard', function () {
        return view('dashboard');
    })->name('dashboard');
});

Route::get('/login', [AuthController::class, 'login'])->name('login')->middleware('guest');
Route::post('/login', [AuthController::class, 'authenticate'])->middleware('guest');
Route::get('/logout', [AuthController::class, 'logout'])->middleware('auth');

Route::get('/stocks', [StockController::class, 'index'])->name('stocks')->middleware('auth');
Route::get('/stock-add', [StockController::class, 'create'])->middleware('auth');
Route::post('/stock', [StockController::class, 'store'])->middleware('auth');
